CREATE TABLE IF NOT EXISTS clusterDB.configurations (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    `name` VARCHAR(64),
    `partition` VARCHAR(64),
    shape VARCHAR(64),
    change_hostname BOOLEAN,
    hostname_convention VARCHAR(64),
    permanent BOOLEAN,
    rdma_enabled BOOLEAN,
    stand_alone BOOLEAN,
    region VARCHAR(64),
    availability_domain VARCHAR(64),
    private_subnet_cidr VARCHAR(64),
    private_subnet_id VARCHAR(128),
    image_id VARCHAR(128),
    target_compartment_id VARCHAR(128),
    boot_volume_size INT,
    use_marketplace_image BOOLEAN,
    instance_pool_ocpus INT,
    instance_pool_custom_memory BOOLEAN,
    instance_pool_memory INT,
    marketplace_listing VARCHAR(64),
    hyperthreading BOOLEAN,
    preemptible BOOLEAN
);