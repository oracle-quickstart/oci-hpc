CREATE TABLE IF NOT EXISTS clusterDB.nodes (
    id INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
    ip_address VARCHAR(128) UNIQUE,
    controller_status ENUM('configuring', 'terminating', 'waiting_for_info', 'configured', 'terminated', 'reconfiguring'),
    started_time VARCHAR(128),
    status ENUM('starting', 'terminating', 'terminated', 'running', 'unreachable'),
    availability_domain VARCHAR(128),
    first_time_reachable VARCHAR(128),
    cluster_name VARCHAR(128),
    compartment_id VARCHAR(128),
    tenancy_id VARCHAR(128),
    compute_status ENUM('configuring', 'configured', 'starting','terminating'),
    controller_name VARCHAR(128),
    fss_mount VARCHAR(128),
    gpu_memory_fabric VARCHAR(128),
    hostname VARCHAR(128) UNIQUE,
    hpc_island VARCHAR(128),
    image_id VARCHAR(128),
    last_time_reachable VARCHAR(128),
    oci_name VARCHAR(128),
    ocid VARCHAR(128) UNIQUE,
    rack_id VARCHAR(128),
    rail_id VARCHAR(128),
    network_block_id VARCHAR(128),
    memory_cluster_name VARCHAR(128),
    memory_fabric_id VARCHAR(128),
    role VARCHAR(128),
    serial VARCHAR(128),
    shape VARCHAR(128),
    terminated_time VARCHAR(128),
    update_count INT,
    healthcheck_recommendation VARCHAR(128),
    last_healthcheck_time VARCHAR(128),
    healthcheck_logs VARCHAR(2048),
    oci_health VARCHAR(128),
    oci_impacted_components BOOLEAN,
    oci_host_id VARCHAR(128)
);


